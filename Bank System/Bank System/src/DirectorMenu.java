            Statement statetemsilci=Conn.createStatement();
            ResultSet result2=statetemsilci.executeQuery(querytemsilci);
            result2.next();
            String temsilci=result2.getString("temsilciid");
            
            String query2="insert into musteri values(?,?,?,?,?,?,?,?)";
