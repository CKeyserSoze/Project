            state2.setString(2, yaz2);
            state2.setString(3, yaz3);
            state2.setString(4, yaz4);
            state2.setString(5, yaz5);
            state2.setString(6, yaz6);
            state2.setString(7, yaz7);
            state2.setString(8, temsilci);
            state2.execute();
            
            String query3="insert into loginekranı values (?,?)";
            PreparedStatement state3=Conn.prepareStatement(query3);
            state3.setString(1, hesapid);
            state3.setString(2, yaz7);
            state3.execute();
            
            dlm.removeRow(row);
